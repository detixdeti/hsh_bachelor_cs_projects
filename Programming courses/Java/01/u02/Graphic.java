public class Graphic   {
    public static void main(String[] args) {
        System.out.println("  _______");
        // Backslash \ ist ein escape character \\ lässt Backslash ausgeben
        System.out.println(" /       \\");
        System.out.println("/         \\");
        // die Anführungsstriche im String muss man mit \ versehen sonst würde
        // der als Ende des Strings angesehen werden
        System.out.println("-\"-'-\"-'-\"-");
        System.out.println("\\         /");
        System.out.println(" \\_______/");
    }
}